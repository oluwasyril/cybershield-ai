// ─────────────────────────────────────────────

const express          = require('express');
const router           = express.Router();
const { db, auth: adminAuth, admin } = require('../services/firebase');
const { authenticate } = require('../middleware/authenticate');
const { sendVerificationEmail, sendPasswordResetEmail } = require('../services/emailService');

// ─────────────────────────────────────────────
// HELPER — replaces Firebase's default handler
// domain with our custom action.html page.
//
// Firebase generates links pointing to:
// https://[project].firebaseapp.com/__/auth/action
//
// We replace that with:
// https://mycybershieldai.web.app/action.html
//
// The oobCode and mode parameters remain valid
// because they are generated by Firebase —
// only the delivery URL changes.
// ─────────────────────────────────────────────

function useCustomHandler(firebaseLink) {
  return firebaseLink.replace(
    /https:\/\/[a-z0-9-]+\.firebaseapp\.com\/__\/auth\/action/,
    `${process.env.FRONTEND_URL}/action.html`
  );
}

// ─────────────────────────────────────────────
// POST /api/user/profile
// ─────────────────────────────────────────────

router.post('/profile', authenticate, async (req, res) => {
  const { firstName, lastName, username, country, displayName } = req.body;
  const uid   = req.user.uid;
  const email = req.user.email;

  try {
    if (username) {
      const taken = await db.collection('users')
        .where('username', '==', username.toLowerCase())
        .get();

      if (taken.docs.some(d => d.id !== uid)) {
        return res.status(409).json({
          success: false,
          error  : 'Username already taken. Please choose another.'
        });
      }
    }

    await db.collection('users').doc(uid).set({
      uid,
      email,
      firstName  : firstName   || '',
      lastName   : lastName    || '',
      username   : username    ? username.toLowerCase() : '',
      displayName: displayName || `${firstName} ${lastName}`,
      country    : country     || '',
      createdAt  : new Date().toISOString(),
      scanCount  : 0
    }, { merge: true });

    res.status(200).json({ success: true });

  } catch (error) {
    console.error('[PROFILE ERROR]', error.message);
    res.status(500).json({ success: false, error: error.message });
  }
});

// ─────────────────────────────────────────────
// GET /api/user/profile
// ─────────────────────────────────────────────

router.get('/profile', authenticate, async (req, res) => {
  try {
    const doc = await db.collection('users').doc(req.user.uid).get();

    if (!doc.exists) {
      return res.status(404).json({ success: false, error: 'Profile not found.' });
    }

    res.status(200).json({ success: true, profile: doc.data() });

  } catch (error) {
    res.status(500).json({ success: false, error: error.message });
  }
});

// ─────────────────────────────────────────────
// PUT /api/user/profile
// ─────────────────────────────────────────────

router.put('/profile', authenticate, async (req, res) => {
  const { firstName, lastName, country } = req.body;

  try {
    await db.collection('users').doc(req.user.uid).update({
      firstName,
      lastName,
      country,
      displayName: `${firstName} ${lastName}`,
      updatedAt  : new Date().toISOString()
    });

    res.status(200).json({ success: true });

  } catch (error) {
    res.status(500).json({ success: false, error: error.message });
  }
});

